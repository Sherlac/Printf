#include "ft_printf.h"

char	*ft_str_tolower(char *str)
{
	if (*str)
	{
		while (*str)
		{
			if (*str >= 'A' && *str <= 'Z')
			{
				*str = *str + 32;
			}			
			printf("%c", *str);
			str++;
		}
	}
	return (str);
}
